title=Démo: programme à trou
text==
Dans ce type d'exercice, l'étudiant doit compléter le(s) trou(s)
(spécifié(s) par les sections "SOLUTION") pour que sa version du
programme donne la même sortie que la version avec solution(s). Les
trous peuvent être préremplis (sections "INIT SOLUTION").

Les instructions sur l'objectif à atteindre sont typiquement données
sous forme de commentaires dans le code.
