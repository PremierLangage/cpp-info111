title=Démo: programme à trou silencieux
text==
Dans ce type d'exercice, l'étudiant doit compléter le(s) trou(s)
(spécifié(s) par les sections "SOLUTION") pour que le programme
n'affiche rien. Le cas d'usage typique est d'implanter une fonction
dont le bon fonctionnement est vérifié au moyen de tests (ASSERT).
Certains de ces tests peuvent être cachés.
